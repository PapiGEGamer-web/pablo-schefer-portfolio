create or replace function public.create_chess_game(player_name text)
returns public.chess_games
language plpgsql
security definer
set search_path = ''
as $$
declare
  created_game public.chess_games;
  safe_name text := left(trim(player_name), 32);
begin
  if auth.uid() is null then
    raise exception 'authentication_required';
  end if;
  if char_length(safe_name) < 3 then
    raise exception 'invalid_player_name';
  end if;
  if (
    select count(*)
    from public.chess_games
    where white_id = auth.uid() and status in ('waiting', 'active')
  ) >= 5 then
    raise exception 'too_many_open_games';
  end if;

  insert into public.chess_games (white_id, white_name)
  values (auth.uid(), safe_name)
  returning * into created_game;
  return created_game;
end;
$$;

create or replace function public.join_chess_game(game_id uuid, player_name text)
returns public.chess_games
language plpgsql
security definer
set search_path = ''
as $$
declare
  joined_game public.chess_games;
  safe_name text := left(trim(player_name), 32);
begin
  if auth.uid() is null then
    raise exception 'authentication_required';
  end if;
  if char_length(safe_name) < 3 then
    raise exception 'invalid_player_name';
  end if;

  update public.chess_games
  set black_id = auth.uid(),
      black_name = safe_name,
      status = 'active'
  where id = game_id
    and status = 'waiting'
    and black_id is null
    and white_id <> auth.uid()
  returning * into joined_game;

  if joined_game.id is null then
    raise exception 'game_not_available';
  end if;
  return joined_game;
end;
$$;

create or replace function public.play_chess_move(
  game_id uuid,
  previous_fen text,
  next_fen text,
  next_pgn text,
  next_turn text,
  move_uci text,
  game_result text default null
)
returns public.chess_games
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_game public.chess_games;
  updated_game public.chess_games;
  expected_player uuid;
begin
  if auth.uid() is null then
    raise exception 'authentication_required';
  end if;
  if char_length(next_fen) not between 20 and 120
     or char_length(next_pgn) > 12000
     or next_turn not in ('w', 'b')
     or move_uci !~ '^[a-h][1-8][a-h][1-8][qrbn]?$'
     or (game_result is not null and game_result not in ('white', 'black', 'draw')) then
    raise exception 'invalid_move_payload';
  end if;

  select * into current_game
  from public.chess_games
  where id = game_id
  for update;

  if current_game.id is null or current_game.status <> 'active' or current_game.fen <> previous_fen then
    raise exception 'stale_game';
  end if;

  expected_player := case when current_game.turn = 'w' then current_game.white_id else current_game.black_id end;
  if expected_player is distinct from auth.uid() then
    raise exception 'not_your_turn';
  end if;

  update public.chess_games
  set fen = next_fen,
      pgn = next_pgn,
      turn = next_turn,
      last_move = move_uci,
      status = case when game_result is null then 'active' else 'finished' end,
      result = game_result,
      winner_id = case
        when game_result = 'white' then current_game.white_id
        when game_result = 'black' then current_game.black_id
        else null
      end
  where id = game_id
  returning * into updated_game;

  return updated_game;
end;
$$;

create or replace function public.resign_chess_game(game_id uuid)
returns public.chess_games
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_game public.chess_games;
  updated_game public.chess_games;
begin
  if auth.uid() is null then
    raise exception 'authentication_required';
  end if;

  select * into current_game
  from public.chess_games
  where id = game_id
  for update;

  if current_game.id is null
     or current_game.status <> 'active'
     or auth.uid() not in (current_game.white_id, current_game.black_id) then
    raise exception 'game_not_available';
  end if;

  update public.chess_games
  set status = 'finished',
      result = 'resigned',
      winner_id = case when auth.uid() = current_game.white_id then current_game.black_id else current_game.white_id end
  where id = game_id
  returning * into updated_game;
  return updated_game;
end;
$$;

