create table if not exists public.chess_games (
  id uuid primary key default gen_random_uuid(),
  white_id uuid not null references auth.users(id) on delete cascade,
  black_id uuid references auth.users(id) on delete set null,
  white_name text not null check (char_length(white_name) between 3 and 32),
  black_name text check (black_name is null or char_length(black_name) between 3 and 32),
  fen text not null default 'rnbqkbnr/pppppppp/8/8/8/8/PPPPPPPP/RNBQKBNR w KQkq - 0 1'
    check (char_length(fen) between 20 and 120),
  pgn text not null default '' check (char_length(pgn) <= 12000),
  turn text not null default 'w' check (turn in ('w', 'b')),
  status text not null default 'waiting' check (status in ('waiting', 'active', 'finished')),
  winner_id uuid references auth.users(id) on delete set null,
  result text check (result is null or result in ('white', 'black', 'draw', 'resigned')),
  last_move text check (last_move is null or char_length(last_move) between 4 and 5),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint chess_players_are_different check (black_id is null or black_id <> white_id)
);

create index if not exists chess_games_status_updated_idx
  on public.chess_games (status, updated_at desc);

alter table public.chess_games enable row level security;
