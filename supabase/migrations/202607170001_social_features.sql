create or replace function public.set_updated_at()
returns trigger
language plpgsql
security invoker
set search_path = ''
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.chat_messages (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  username text not null check (char_length(username) between 3 and 32),
  body text not null check (char_length(body) between 1 and 600),
  created_at timestamptz not null default now()
);

create index if not exists chat_messages_created_at_idx
  on public.chat_messages (created_at desc);

alter table public.chat_messages enable row level security;
revoke all on public.chat_messages from anon, authenticated;
grant select on public.chat_messages to authenticated;
grant usage, select on sequence public.chat_messages_id_seq to authenticated;

drop policy if exists "Account holders read chat" on public.chat_messages;
create policy "Account holders read chat"
  on public.chat_messages
  for select
  to authenticated
  using (true);

drop policy if exists "Account holders write own chat messages" on public.chat_messages;

create or replace function public.send_chat_message(message_body text, display_name text)
returns public.chat_messages
language plpgsql
security definer
set search_path = ''
as $$
declare
  created_message public.chat_messages;
  safe_body text := left(trim(message_body), 600);
  safe_name text := left(trim(display_name), 32);
  recent_count integer;
begin
  if auth.uid() is null then
    raise exception 'authentication_required';
  end if;
  if char_length(safe_body) < 1 or char_length(safe_name) < 3 then
    raise exception 'invalid_message';
  end if;

  select count(*) into recent_count
  from public.chat_messages
  where user_id = auth.uid() and created_at > now() - interval '30 seconds';
  if recent_count >= 5 then
    raise exception 'chat_rate_limited';
  end if;

  select count(*) into recent_count
  from public.chat_messages
  where user_id = auth.uid() and created_at > now() - interval '1 hour';
  if recent_count >= 80 then
    raise exception 'chat_rate_limited';
  end if;

  insert into public.chat_messages (user_id, username, body)
  values (auth.uid(), safe_name, safe_body)
  returning * into created_message;
  return created_message;
end;
$$;

revoke all on function public.send_chat_message(text, text) from public, anon;
grant execute on function public.send_chat_message(text, text) to authenticated;

